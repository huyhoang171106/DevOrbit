create index if not exists idx_github_repos_course     on github_repos(course_id);
create index if not exists idx_github_repos_active     on github_repos(is_active);
create index if not exists idx_repo_candidates_status  on repo_candidates(status);
create index if not exists idx_roadmaps_student        on learning_roadmaps(student_id);
create index if not exists idx_roadmap_phases_roadmap  on roadmap_phases(roadmap_id);
create index if not exists idx_roadmap_items_phase     on roadmap_items(phase_id);
create index if not exists idx_course_rels_course      on course_relationships(course_id);
create index if not exists idx_notes_student           on notes(student_id);
create index if not exists idx_notes_target            on notes(target_type, target_id);
create index if not exists idx_note_snippets_note      on note_code_snippets(note_id);
create index if not exists idx_course_yt_course        on course_youtube_playlists(course_id);
create index if not exists idx_course_articles_course  on course_articles(course_id);
create index if not exists idx_course_tutorials_course on course_tutorials(course_id);

