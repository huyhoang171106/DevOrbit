-- ============================================================
-- 0. CLEANUP (Ensures schema updates apply)
-- ============================================================
DROP TABLE IF EXISTS course_relationships CASCADE;
DROP TABLE IF EXISTS course_tutorials CASCADE;
DROP TABLE IF EXISTS course_articles CASCADE;
DROP TABLE IF EXISTS course_youtube_playlists CASCADE;
DROP TABLE IF EXISTS repo_candidates CASCADE;
DROP TABLE IF EXISTS repo_tech_stacks CASCADE;
DROP TABLE IF EXISTS github_repos CASCADE;
DROP TABLE IF EXISTS tech_stacks CASCADE;
DROP TABLE IF EXISTS otps CASCADE;
DROP TABLE IF EXISTS roadmap_items CASCADE;
DROP TABLE IF EXISTS roadmap_phases CASCADE;
DROP TABLE IF EXISTS learning_roadmaps CASCADE;
DROP TABLE IF EXISTS notes CASCADE;
DROP TABLE IF EXISTS note_code_snippets CASCADE;
DROP TABLE IF EXISTS courses CASCADE;
DROP TABLE IF EXISTS student_users CASCADE;
DROP TABLE IF EXISTS admin_users CASCADE;

-- ============================================================
-- 1. USERS & AUTH
-- ============================================================
create table if not exists admin_users (
    id            bigint generated by default as identity primary key,
    username      varchar(255) not null unique,
    password_hash varchar(255) not null,
    active        boolean      not null default true
);

create table if not exists student_users (
    id            bigint generated by default as identity primary key,
    student_code  varchar(255) not null unique,
    full_name     varchar(255) not null,
    email         varchar(255) not null unique,
    password_hash varchar(255) not null,
    active        boolean      not null default true
);

create table if not exists student_bookmarks (
    id          bigint generated by default as identity primary key,
    student_id   bigint       not null references student_users(id) on delete cascade,
    target_type varchar(20)  not null,
    target_id   bigint       not null,
    title       varchar(255) not null,
    subtitle    varchar(255),
    url         varchar(255) not null,
    created_at  timestamp    not null default now(),
    unique (student_id, target_type, target_id)
);

create table if not exists otps (
    id          bigint generated by default as identity primary key,
    email       varchar(255) not null,
    otp_code    varchar(6)   not null,
    expires_at  timestamp    not null,
    created_at  timestamp    not null default now()
);

-- ============================================================
-- 6. PHOTOBOOTH — custom frame definitions
-- ============================================================
create table if not exists photobooth_frames (
    id                 uuid        default gen_random_uuid() primary key,
    frame_id           text        not null unique,
    name               text        not null,
    display_name       text        not null,
    photo_count        integer     not null check (photo_count in (1, 2, 3, 4, 6)),
    description        text        default '',
    slots              jsonb       not null default '[]'::jsonb,
    overlay_image_url  text,
    filter             text        default 'normal',
    background_color   text        default '#ffffff',
    created_at         timestamp   not null default now(),
    updated_at         timestamp   not null default now()
);


-- ============================================================
-- 2. SUBJECT ORBIT — Courses
-- ============================================================
create table if not exists courses (
    stt        bigint generated by default as identity primary key,
    mamh       varchar(20)  not null unique,
    tenmh        varchar(255) not null,
    tenmh_en     varchar(255),
    sotc         integer      not null default 0,
    lt           integer      not null default 0,
    th           integer      not null default 0,
    loaimonhoc   varchar(50)  not null,
    semester     integer,
    is_open      boolean      not null default true,
    management_unit varchar(100),
    description  text,
    mamh_old     varchar(50),
    equivalent_mh text,
    prerequisite_mh text,
    previous_mh  text
);

-- External learning resources linked to each course
create table if not exists course_youtube_playlists (
    id          bigint generated by default as identity primary key,
    course_id   bigint       not null references courses(stt) on delete cascade,
    title       varchar(255) not null,
    url         varchar(255) not null,
    description text,
    channel_name varchar(255),
    created_at  timestamp    not null default now()
);

create table if not exists course_articles (
    id          bigint generated by default as identity primary key,
    course_id   bigint       not null references courses(stt) on delete cascade,
    title       varchar(255) not null,
    url         varchar(255) not null,
    author      varchar(255),
    description text,
    created_at  timestamp    not null default now()
);

create table if not exists course_tutorials (
    id          bigint generated by default as identity primary key,
    course_id   bigint       not null references courses(stt) on delete cascade,
    title       varchar(255) not null,
    url         varchar(255) not null,
    type        varchar(50)  not null default 'article',
    description text,
    created_at  timestamp    not null default now()
);

-- ============================================================
-- 3. LEGACY REPO FINDER — GitHub repos & tech stacks
-- ============================================================
create table if not exists tech_stacks (
    id      bigint generated by default as identity primary key,
    name    varchar(120) not null,
    repo_id bigint       -- legacy column; new code uses join table
);

create unique index if not exists uk_tech_stack_name on tech_stacks (name);

create table if not exists github_repos (
    id               bigint generated by default as identity primary key,
    repo_name        varchar(255)  not null,
    description      text,
    github_url       varchar(255)  not null,
    tech_stack       varchar(255),
    subject_id       varchar(20),
    display_name     varchar(255),
    primary_language varchar(100),
    is_active        boolean       not null default true,
    course_id        bigint        references courses(stt),
    stars            integer
);

create table if not exists repo_tech_stacks (
    repo_id       bigint not null references github_repos(id) on delete cascade,
    tech_stack_id bigint not null references tech_stacks(id) on delete cascade,
    primary key (repo_id, tech_stack_id)
);

DROP TYPE IF EXISTS repo_candidate_status CASCADE;
CREATE TYPE repo_candidate_status AS ENUM (
    'NEW', 'APPROVED', 'REJECTED'
);

DROP TYPE IF EXISTS roadmap_item_target_type CASCADE;
CREATE TYPE roadmap_item_target_type AS ENUM (
    'COURSE', 'REPO'
);

create table if not exists repo_candidates (
    id               bigint generated by default as identity primary key,
    course_id        bigint                 references courses(stt),
    scan_query       varchar(255),
    github_owner     varchar(255),
    github_name      varchar(255),
    github_url       varchar(255) not null,
    description      text,
    primary_language varchar(100),
    topics           text,
    stars            integer,
    forks            integer,
    last_pushed_at   varchar(255),
    readme_excerpt   text,
    status           varchar(20) not null default 'NEW',
    review_note      text,
    assigned_reviewer varchar(50)
);

create table if not exists learning_roadmaps (
    id               bigint generated by default as identity primary key,
    student_id       bigint       not null references student_users(id) on delete cascade,
    title            varchar(255) not null,
    description      text,
    markdown_content text,
    is_public        boolean      not null default false,
    created_at       timestamp    not null default now(),
    updated_at       timestamp    not null default now()
);

create table if not exists roadmap_phases (
    id          bigint generated by default as identity primary key,
    roadmap_id  bigint       not null references learning_roadmaps(id) on delete cascade,
    title       varchar(255) not null,
    description text,
    sort_order  integer      not null default 0,
    created_at  timestamp    not null default now()
);

create table if not exists roadmap_items (
    id          bigint generated by default as identity primary key,
    phase_id    bigint                    not null references roadmap_phases(id) on delete cascade,
    target_type roadmap_item_target_type  not null,
    target_id   bigint                    not null,
    title       varchar(255),
    note        text,
    sort_order  integer                   not null default 0,
    created_at  timestamp                 not null default now()
);

DROP TYPE IF EXISTS course_relation_type CASCADE;
CREATE TYPE course_relation_type AS ENUM (
    'PREREQUISITE',
    'COMPLEMENTARY',
    'COREQUISITE'
);

DROP TYPE IF EXISTS note_target_type CASCADE;
CREATE TYPE note_target_type AS ENUM (
    'COURSE', 'REPO', 'NONE'
);

create table if not exists course_relationships (
    id              bigint                 generated by default as identity primary key,
    course_id       bigint                 not null references courses(stt) on delete cascade,
    related_course_id bigint               not null references courses(stt) on delete cascade,
    relation_type   course_relation_type   not null,
    created_at      timestamp              not null default now(),
    unique (course_id, related_course_id),
    check (course_id <> related_course_id)
);

-- ============================================================
-- 7. SPLIT-VIEW NOTE EDITOR — Notes & code snippets
-- ============================================================
DROP TYPE IF EXISTS note_target_type CASCADE;
CREATE TYPE note_target_type AS ENUM (
    'COURSE', 'REPO', 'NONE'
);

create table if not exists notes (
    id              bigint generated by default as identity primary key,
    student_id      bigint            not null references student_users(id) on delete cascade,
    title           varchar(255)      not null,
    content_markdown text,
    target_type     note_target_type  not null default 'NONE',
    target_id       bigint,
    created_at      timestamp         not null default now(),
    updated_at      timestamp         not null default now()
);

create table if not exists note_code_snippets (
    id         bigint generated by default as identity primary key,
    note_id    bigint       not null references notes(id) on delete cascade,
    language   varchar(50)  not null,
    code       text         not null,
    caption    varchar(255),
    sort_order integer      not null default 0
);

-- ============================================================
-- INDEXES for performance
