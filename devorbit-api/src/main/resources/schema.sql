-- ============================================================
-- DevOrbit Complete Database Schema
-- Covers all 5 features:
--   1. Subject Orbit (courses + external resources)
--   2. Legacy Repo Finder (github repos, candidates, tech stacks)
--   3. Learning Roadmap (markdown plans with phased items)
--   4. Knowledge Graph (course prerequisite relationships)
--   5. Split-view Note Editor (notes + code snippets)
-- ============================================================

